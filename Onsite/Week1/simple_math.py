""" Rewind-SimpleMath """
def main():
    """ Main function """
    var_a = int(input())
    var_b = int(input())

    print(var_a + var_b)
    print(var_a - var_b)
    print(var_a * var_b)
    print(var_a / var_b)
    print(var_a // var_b)
    print(var_a % var_b)
    print(var_a ** var_b)

main()
